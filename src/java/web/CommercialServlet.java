    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        send(request, response);
    }

    public void send(HttpServletRequest request, HttpServletResponse response) throws IOException, ServletException{
        int maths = Integer.parseInt(request.getParameter("maths"));
        int english = Integer.parseInt(request.getParameter("english"));
        int marketing = Integer.parseInt(request.getParameter("marketing"));
        int commerce = Integer.parseInt(request.getParameter("commerce"));
        int government = Integer.parseInt(request.getParameter("government"));
        String field = "commercial";
        int total = maths + english + marketing + commerce + government;
        request.setAttribute("total", total);
        request.setAttribute("field", field);
        RequestDispatcher dispatch = request.getRequestDispatcher("home/personality_test/index.jsp");
        dispatch.include(request, response);
    }
    
